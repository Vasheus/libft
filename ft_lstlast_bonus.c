#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*current;

	if (!lst)
		return (NULL);
	current = lst;
	while (current != NULL)
	{
		if (!current->next)
			return (current);
		current = current->next;
	}
	return (current);
}
